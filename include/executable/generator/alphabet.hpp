[[maybe_unused]] static void append(std::string&) {}

template <typename... Args>
static void append(std::string& result, uint8_t c1, Args... cx) {
  result.append(1, (char) c1);
  append(result, cx...);
}

template <typename... Args>
static std::string concat(Args... cx) {
  std::string result = "";
  append(result, cx...);
  return result;
}